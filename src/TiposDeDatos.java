
public class TiposDeDatos
{
    public static void main(String[] args)
    {
        //Se declara una variable de tipo entero
        int var1;
        var1 = 10;
        System.out.println(var1);

        //Se declara una variable de tipo Integer
        Integer var2;
        var2 = 2;
        System.out.println(var2);

        //Se declara una variable de tipo nulo
        Integer var3;
        var3 = null;
        System.out.println(var3);

        //Se declara una variable de tipo double
        double var4;
        var4 = 20.5;
        System.out.println(var4);

        //Se declara una variable de tipo String
        String cadena;
        cadena = "Hola estoy haciendo el curso de java";
        System.out.println(cadena);

        //Se declara una variable de tipo boolean
        boolean var5;
        var5 = true;
        System.out.println(var5);

        //Se declara una variable de tipo byte
        byte var7;
        var7 = 10;
        System.out.println(var7);

        //Se declara una variable de tipo short
        short var8;
        var8 = 16;
        System.out.println(var8);

        //Se declara una variable de tipo long
        long var9;
        var9 = 14;
        System.out.println(var9);

        //Se declara una variable de tipo Number
        int x = 9;
        Integer y = 99;
        System.out.println(y);

        //Se declara una variable de tipo String en la que para realizar
        //la suma se debera primero convertir el número  tipo númerico
        String texto = "99";
        int suma = Integer.parseInt(texto)+1;
        System.out.println(suma);
    }
}
